package com.callor.Var;

/* 
 * 실수를 표현하기 2
 * float 
 */
public class VarFloat {
	
	public static void main(String[] args) {
		/*
		 * float형(단정도실수형) 변수 num1을 선언하고
		 * float형 값 50.0을 저장하라.
		 * 이때 숫자값에는 F(또는 f)를 붙여야 한다.
		 */
		
		float num1 = 50.0F;
		float num2 = 50.1111111111111F;
		System.out.println(num1);
		System.out.println(num2);
		
	}

}
